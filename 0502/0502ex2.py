for i in range(10) : #반복조건
    print(i, end=' ') #반복조건 참인 동안 반복 # end 공백 추가

print()

for j in range(1, 10) : #반복조건
    print(j, end=' ')   

print()

for k in range(1, 11, 2) : #반복조건
    print(k, end=' ') #홀수 출력

print()

for l in range(10, 0, -1) : #반복조건
    print(l, end=' ') #역순 출력